package com.pojo;

public class SignInFamily {
	private String family_alias;
	private String family_name;
	private String family_born_year;
	private String family_born_month;
	private String family_born_day;
	private String political;
	private String work_organization;
	public String getFamily_alias() {
		return family_alias;
	}
	public void setFamily_alias(String family_alias) {
		this.family_alias = family_alias;
	}
	public String getFamily_name() {
		return family_name;
	}
	public void setFamily_name(String family_name) {
		this.family_name = family_name;
	}
	public String getFamily_born_year() {
		return family_born_year;
	}
	public void setFamily_born_year(String family_born_year) {
		this.family_born_year = family_born_year;
	}
	public String getFamily_born_month() {
		return family_born_month;
	}
	public void setFamily_born_month(String family_born_month) {
		this.family_born_month = family_born_month;
	}
	public String getFamily_born_day() {
		return family_born_day;
	}
	public void setFamily_born_day(String family_born_day) {
		this.family_born_day = family_born_day;
	}
	public String getPolitical() {
		return political;
	}
	public void setPolitical(String political) {
		this.political = political;
	}
	public String getWork_organization() {
		return work_organization;
	}
	public void setWork_organization(String work_organization) {
		this.work_organization = work_organization;
	}
	
}
